def main():
    pygame.init()
    
    #Spielfigur Variablen
    player_x = 32 #Startposition X
    player_y=32 #Startposition Y
    player_size = 25 #Breite und Höhe des Quadrats
    player_color = (0, 255, 0) #Grün
    
    min_x, min_y = 0, 0
    max_x = WIDTH - player_size
    max_y = HEIGHT - player_size
    
    
    
    screen = pygame.display.set_mode([WIDTH, HEIGHT])
    pygame.display.set_caption ("Virus Escape - Schritt 1")
    timer = pygame.time.Clock()
    
    running = True
    while running:
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                running = False
            elif event.type == pygame.KEYDOWN and event.key == pygame.K_ESCAPE:
                running = False
                
        #Bewegung der Spielfigur
        keys = pygame.key.get_pressed()
        if keys[pygame.K_RIGHT]:
            player_x += 5
        if keys[pygame.K_LEFT]:
            player_x -= 5 
        if keys[pygame.K_UP]:
            player_y -= 5 
        if keys[pygame.K_DOWN]:
            player_y += 5     
            
        if player_x < min_x: player_x = min_x
        if player_x > max_x: player_x = max_x
        if player_y < min_y: player_y = min_y
        if player_y > max_y: player_y = max_y
        
        screen.fill((10, 10, 15))
        
        for row_index, row in enumerate(boards):
            for col_index, tile in enumerate(row):
                color = TILE_COLORS[tile]
                pygame.draw.rect(screen, color,
                                 (col_index * TILE_SIZE, row_index * TILE_SIZE, TILE_SIZE, TILE_SIZE))
                
        pygame.draw.rect(screen, player_color, (player_x, player_y, player_size, player_size))
        pygame.display.flip()
        timer.tick(60)
    
    pygame.quit()
    sys.exit()

if __name__ == "__main__": 
    main()